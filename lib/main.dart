class MyApp extends StatefulWidget {
  const MyApp({super.key});

  @override
  State<MyApp> createState() => _MyAppState();
}

class _MyAppState extends State<MyApp> {

  final Size canvasSize = Size(W*F, H*F);

  _MyAppState(){
    for (var y=0; y<H; y++)
    {
      for (var x=0; x<W; x++)
      {
        if (lines[y][x] == '#')
        {
          items.add(Point(x,y));
        }
      }
    }
  }

  Set<Point> items = {};

  Iterable<Point> neighbours_of(Point p) sync*
  {
      for (var dy=-1; dy<=1; dy++)
      {
        for (var dx=-1; dx<=1; dx++)
        {
          var x = p.x + dx;
          var y = p.y + dy;

          if (x < 0 || x >= W) continue;
          if (y < 0 || y >= H) continue;
          if (dx==0 && dy==0) continue;

          yield Point(x,y);
        }
      }
  }

  void startUpdates() async {
    for (int i=0; i<100; i++) {
      await Future.delayed(Duration(milliseconds: 10));
      updateItems();
    }
  }

  void updateItems() {
    setState(() {

      List<Point> to_remove = [];
      List<Point> to_add = [];

      for (var y=0; y<H; y++)
      {
        for (var x=0; x<W; x++)
        {
          var p = Point(x,y);
          var ns = neighbours_of(p).where((n) => items.contains(n)).length;
          if (items.contains(p))
          {
            if (ns < 2 || ns > 3) 
            {
              to_remove.add(p);
            }
          }
          else
          {
            if (ns == 3)
            {
              to_add.add(p);
            } 
          }
        }
      }
      items.addAll(to_add);
      items.removeAll(to_remove);

      items = items.map((offset) {
        return offset;
      }).toSet();
    });
  }

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      debugShowCheckedModeBanner: false,
      home: Scaffold(
        backgroundColor: Color(0xff000435),
        body: Center(
          child: Column(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              CustomPaint(
                size: canvasSize,
                painter: ItemPainter(
                  items: items,
                ),
              ),
              SizedBox(height: 70),
              ElevatedButton(
                onPressed: startUpdates,
                child: Text('Update Items'),
              ),
            ],
          ),
        ),
      ),
    );
  }
}

class ItemPainter extends CustomPainter {
  final Set<Point> items;

  ItemPainter({required this.items});

  @override
  void paint(Canvas canvas, Size size) {

    var paths = items.map((s) {
      return Path()
        ..addRect(Rect.fromPoints(Offset(s.x*F,s.y*F), Offset(s.x*F+F,s.y*F+F)));
    }).toList();

    var paints = items.map((s) {
      return Paint()
        ..style = PaintingStyle.fill
        ..color = Colors.white.withOpacity(1.0);
    }).toList();

    for (int i = 0; i < paths.length; i++) {
      canvas.drawPath(paths[i], paints[i]);
    }
  }

  @override
  bool shouldRepaint(covariant CustomPainter oldDelegate) {
    return true;
  }
}